#Tipo de dado bool (boolean)
#Ao questionar algo em um programa, so existem duas respostas possiveis:
#sim (True)  ou nao(False).
#Existem varios operadores para question.
#dentr eles o ==, que e um operador logico que questiona se um valor e igual ao outro. 
igual = 10
print(igual == 10) # sim => True
print(igual == 102)# nao => False
print(type(igual == 102))# nao => False