#Tipos int e float
#int -> Numero inteiro
#O tipo inte representa qualquer numero
#positivo ou negativo. int sem sinal e considerao positivo
print(11) #int
print(-11)#negativo

#float -> numero com ponto flutuante
#O tipo float representa qualquer numero 
#positivo ou negativo flutuante
#float sem sinal e considerado positivo
print(1.1, 10.11) #int
print(-11)#negativo

# A funcao type mostra o tipo que o Python inferuiu ao valor
print(type(-11))
print(type('A'))
